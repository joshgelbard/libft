#include "libft.h"

size_t	ft_numlen(int n)
{
	size_t	cc;

	cc = 1;
	while ((unsigned int)(n < 0 ? n * -1 : n) > 9)
	{
		++cc;
		n /= 10;
	}
	return (cc);
}
