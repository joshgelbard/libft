char	*ft_bytestr(void *p, size_t len)
{
	char	*s;
	size_t	i;

	if (!(s = malloc(len + 1)))
		return (NULL);
	i = 0;
	while (i < len)
	{
		s[i] = p[i] + (p[i] < 10 ? '0' : 'a');
		++i;
	}
	s[i] = '\0';
	return (s);
}
