char	*ft_bitstr(void *p, size_t len)
{
	char	*s;
	size_t	i;
	size_t	j;

	if (!(s = malloc(len * 8 + 1)))
		return (NULL);
	i = 0;
	while (i < len)
	{
		j = 0;
		while (j < 8)
		{
			s[i * 8 + j] = j & p[i];
			++j;
		}
		++i;
	}
	s[len * 8] = '\0';
	return (s);
}
