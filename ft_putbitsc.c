#include "libft.h"

void	ft_putbitsc(unsigned char n)
{
	ft_putchar('0' + !!(n & 8));
	ft_putchar('0' + !!(n & 4));
	ft_putchar('0' + !!(n & 2));
	ft_putchar('0' + !!(n & 1));
	ft_putchar('\n');
}
