#include <string.h>

size_t	ft_strlcat(char *dst, const char *src, size_t dstsize)
{
	size_t	i;
	size_t	j;

	i = 0;
	j = 0;
	while (dst[i] && i < dstsize)
		i++;
	while (src[j] && i < dstsize && j < dstsize - i - 1)
	{
		dst[i + j] = src[j];
		j++;
	}
	while (src[j])
		j++;
	return (i + j);
}
